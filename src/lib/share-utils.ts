export function getWhatsAppUrl(text: string, phone?: string) {
    const encodedText = encodeURIComponent(text);
    if (phone) {
        const normalized = phone.replace(/\D/g, '');
        const withCountry = normalized.length <= 10 ? `91${normalized}` : normalized;
        return `https://wa.me/${withCountry}?text=${encodedText}`;
    }
    return `https://wa.me/?text=${encodedText}`;
}

export function shareViaWhatsApp(text: string, phone?: string) {
    if (typeof window === 'undefined') return;
    window.open(getWhatsAppUrl(text, phone), '_blank');
}

export function shareReportOnWhatsApp(reportTitle: string, data: any, phoneNumber?: string) {
    const text = `*SmartVyapar Report*\n\n*Report:* ${reportTitle}\n*Date:* ${new Date().toLocaleDateString()}\n\n*Summary:*\n${Object.entries(data).map(([k, v]) => `- ${k}: ${v}`).join('\n')}\n\n_Generated by SmartVyapar AI OS_`;
    shareViaWhatsApp(text, phoneNumber);
}

export function shareSitePhotosViaWhatsApp(opts: {
    user?: string;
    address?: string;
    timestamp?: string | Date;
    stockSnapshot?: { name: string; qty: number }[];
    phone?: string;
}) {
    const lines = ['*SmartVyapar — Site Photo*'];
    if (opts.user) lines.push(`👤 ${opts.user}`);
    if (opts.address) lines.push(`📍 ${opts.address}`);
    if (opts.timestamp) {
        const d = typeof opts.timestamp === 'string' ? new Date(opts.timestamp) : opts.timestamp;
        lines.push(`🕐 ${d.toLocaleString()}`);
    }
    if (opts.stockSnapshot && opts.stockSnapshot.length > 0) {
        lines.push('\n*Stock snapshot:*');
        opts.stockSnapshot.forEach((s) => lines.push(`• ${s.name} × ${s.qty}`));
    }
    lines.push('\n_Sent via SmartVyapar_');
    shareViaWhatsApp(lines.join('\n'), opts.phone);
}

