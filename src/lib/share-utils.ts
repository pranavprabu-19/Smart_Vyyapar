export interface InvoiceReminderData {
    invoiceNo: string;
    invoiceDate: string | Date;
    customerName: string;
    totalAmount: number;
    paidAmount?: number;
    balance?: number;
    dueDate?: string | Date;
    daysPastDue?: number;
    items: { description: string; quantity: number; price: number }[];
    outstandingBalance?: number;
    companyName: string;
    companyPhone?: string;
}

/**
 * Format currency in Indian format
 */
function formatCurrency(amount: number): string {
    return new Intl.NumberFormat('en-IN', {
        style: 'currency',
        currency: 'INR',
        maximumFractionDigits: 0,
    }).format(amount);
}

/**
 * Format date in readable format
 */
function formatDateStr(date: string | Date): string {
    const d = typeof date === 'string' ? new Date(date) : date;
    return new Intl.DateTimeFormat('en-IN', {
        day: '2-digit',
        month: 'short',
        year: 'numeric',
    }).format(d);
}

/**
 * Share invoice payment reminder via WhatsApp
 */
export function shareInvoiceReminderViaWhatsApp(
    data: InvoiceReminderData,
    phone?: string
): void {
    const itemsList = data.items
        .map((item) => `• ${item.description} (${item.quantity} × ${formatCurrency(item.price)})`)
        .join('\n');

    const balance = data.balance ?? (data.totalAmount - (data.paidAmount ?? 0));

    const lines = [
        `Dear ${data.customerName},`,
        '',
        'This is a reminder regarding your pending payment.',
        '',
        '*Invoice Details:*',
        `📄 Invoice No: ${data.invoiceNo}`,
        `📅 Date: ${formatDateStr(data.invoiceDate)}`,
        `💰 Amount: ${formatCurrency(data.totalAmount)}`,
    ];

    if (data.paidAmount && data.paidAmount > 0) {
        lines.push(`✅ Paid: ${formatCurrency(data.paidAmount)}`);
    }

    lines.push(`⏳ *Balance Due: ${formatCurrency(balance)}*`);

    if (data.dueDate) {
        lines.push(`📆 Due Date: ${formatDateStr(data.dueDate)}`);
    }

    if (data.daysPastDue && data.daysPastDue > 0) {
        lines.push(`⚠️ Days Overdue: ${data.daysPastDue}`);
    }

    lines.push('', '*Items:*', itemsList);

    if (data.outstandingBalance && data.outstandingBalance !== balance) {
        lines.push('', `*Total Outstanding: ${formatCurrency(data.outstandingBalance)}*`);
    }

    lines.push(
        '',
        'Please make the payment at your earliest convenience.',
        '',
        'Regards,',
        data.companyName
    );

    if (data.companyPhone) {
        lines.push(`📞 ${data.companyPhone}`);
    }

    shareViaWhatsApp(lines.join('\n'), phone);
}

