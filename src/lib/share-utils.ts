/** Derive a sensible .pdf filename from our uploaded URL path. */
export function filenameFromPdfUrl(pdfUrl: string, fallback = "invoice.pdf"): string {
    try {
        const path = new URL(pdfUrl, typeof window !== "undefined" ? window.location.origin : "http://localhost").pathname;
        const base = path.split("/").pop();
        if (base && /\.pdf$/i.test(base)) return decodeURIComponent(base);
    } catch {
        /* ignore */
    }
    return fallback.endsWith(".pdf") ? fallback : `${fallback}.pdf`;
}

/**
 * Save the PDF to the user’s Downloads folder so they can attach it in WhatsApp.
 * Falls back to opening the URL in a new tab if fetch fails (e.g. cross-origin).
 */
export async function downloadPdfFromUrl(pdfUrl: string, filename?: string): Promise<void> {
    if (typeof window === "undefined") return;
    const name = filename || filenameFromPdfUrl(pdfUrl);
    try {
        // Prefer same-site relative path so localhost vs 127.0.0.1 still works.
        let reqUrl = pdfUrl;
        try {
            const parsed = new URL(pdfUrl, window.location.href);
            if (parsed.pathname.startsWith("/")) {
                reqUrl = `${parsed.pathname}${parsed.search}`;
            }
        } catch {
            /* use pdfUrl as-is */
        }
        const res = await fetch(reqUrl, { credentials: "same-origin" });
        if (!res.ok) throw new Error(`HTTP ${res.status}`);
        const blob = await res.blob();
        const objectUrl = URL.createObjectURL(blob);
        const a = document.createElement("a");
        a.href = objectUrl;
        a.download = name;
        a.rel = "noopener";
        document.body.appendChild(a);
        a.click();
        a.remove();
        URL.revokeObjectURL(objectUrl);
    } catch (e) {
        console.error("downloadPdfFromUrl:", e);
        window.open(pdfUrl, "_blank", "noopener,noreferrer");
    }
}
